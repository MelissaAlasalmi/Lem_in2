/*
**	Here we check if the character given is either a flag or a type.
*/

int	is_t(char c)
{
	return ((c == 'c') || (c == 's') || (c == 'p') || (c == 'd') || (c == 'i')
	|| (c == 'u') || (c == 'x') || (c == 'X') || (c == '%')) || (c == 'o')
	|| (c == 'f');
}

int	is_f(char c)
{
	return ((c == '-') || (c == ' ') || (c == '0') || (c == '.') || (c == '*')
	|| (c == 'h')) || (c == 'l') || (c == '#') || (c == ' ') || (c == '+')
	|| (c == 'L') || (c == 'j');
}
